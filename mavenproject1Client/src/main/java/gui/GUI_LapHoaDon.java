    private void xuLyThemVaoTable() throws java.rmi.RemoteException {
        String ma = txtMaSP.getText();
        if (ma.trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn sản phẩm", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String ten = txtTenSP.getText();
        String loai = txtLoaiSP.getText();
        if (txtSoLuong.getText().trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập số lượng", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if (!txtSoLuong.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "Số lượng phải là số dương", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        int soLuong = Integer.parseInt(txtSoLuong.getText());
        String donGia = txtDonGia.getText();
//        SanPhamImpl ds = new SanPhamImpl();
        SanPham sp = daoSP.getSanPhamTheoMa(ma);

        int soLuongConLai = sp.getSoLuong();

        if (soLuong <= 0) {
            JOptionPane.showMessageDialog(null, "Số lượng phải lớn hơn 0", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        if (soLuongConLai <= 0) {
            JOptionPane.showMessageDialog(null, ten + " đã hết hàng", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if (soLuong > soLuongConLai) {
            JOptionPane.showMessageDialog(null, "Số lượng " + ten + " chỉ còn lại " + soLuongConLai, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        xoaRong();
        for (int i = 0; i < tblChiTietHD.getRowCount(); i++) {
            String maTbl = tblChiTietHD.getValueAt(i, 0).toString();
            if (maTbl.equals(ma)) {
                int soLuongAdd = Integer.parseInt(tblChiTietHD.getValueAt(i, 3) + "");
                soLuongAdd += soLuong;
                donGia = donGia.replace(",", "");
                double donGiaSP = Double.parseDouble(donGia);

                tblChiTietHD.setValueAt(soLuongAdd, i, 3);
                tblChiTietHD.setValueAt(dcf.format(soLuongAdd * donGiaSP), i, 5);

                // cập nhật lại số lượng trong db
                daoSP.capNhatSoLuongSP(ma, -soLuong);
                // spBUS.capNhatSoLuongSP(ma, -soLuong);

                return;
            }
        }
        donGia = donGia.replace(",", "");
        double donGiaSP = Double.parseDouble(donGia);
        Vector vec = new Vector();
        vec.add(ma);
        vec.add(ten);
        vec.add(loai);
        vec.add(soLuong);
        vec.add(donGiaSP);
        vec.add(dcf.format(soLuong * donGiaSP));
        daoSP.capNhatSoLuongSP(ma, -soLuong);

        dataModel.addRow(vec);
    }

