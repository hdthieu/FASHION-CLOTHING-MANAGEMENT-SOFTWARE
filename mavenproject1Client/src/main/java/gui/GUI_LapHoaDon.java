    private String phatSinhMaHD(List<HoaDon> hd) {
        int maxCode = 0;
        for (HoaDon hoaDon : hd) {
            String maHD = hoaDon.getMaHD();
            int code = Integer.parseInt(maHD.substring(2));
            if (code > maxCode) {
                maxCode = code;
            }
        }
        // Tạo mã khách hàng mới bằng cách tăng số lên 1 đơn vị
        maxCode++;
        if (maxCode < 1000) {
            String newCode = "HD" + String.format("%03d", maxCode);
            return newCode;
        } else if (maxCode < 10000) {
            String newCode = "HD" + String.format("%04d", maxCode);
            return newCode;
        } else {
            // Xử lý khi mã vượt quá 9999
            JOptionPane.showMessageDialog(null, "Mã số đã đạt tối đa, cần cập nhật thêm", "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
            return null;
        }
    }

