    private void updateAccountTableData() throws java.rmi.RemoteException {

        //TaiKhoanImpl account_DAO = new TaiKhoanImpl();
        List<TaiKhoan> accounts = tkDao.docTuBang();
        dataModel.setRowCount(0);

        for (TaiKhoan account : accounts) {
            //NhanVienImpl ds = new NhanVienImpl();
            NhanVien nhanVien = nvDao.getNhanVienTheoMa(account.getMaNhanVien().getMaNhanVien());
            String[] rowData = {
                account.getMaNhanVien().getMaNhanVien(),
                nhanVien.getTenNhanVien(),
                account.getTenTaiKhoan(),
                account.getMatKhau(),
                account.getQuyenTruyCap(),};
            dataModel.addRow(rowData);
        }
        tblDSTaiKhoan.setModel(dataModel);
    }

    private TaiKhoan revertAccountFormTextFile() {
        String maNhanVien = cboMaNhanVien.getSelectedItem().toString();
        String tenTaiKhoan = txtTenTaiKhoan.getText();
        String matKhau = txtMatKhau.getText();
        String quyenTruyCap = cboQuyenTruyCap.getSelectedItem().toString();
        if (tenTaiKhoan.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa điền tên tài khoản", "Lỗi dữ liệu", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        if (matKhau.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa điền mật khẩu", "Lỗi dữ liệu", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        return new TaiKhoan(new NhanVien(maNhanVien), tenTaiKhoan, matKhau, quyenTruyCap);
    }

