    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        try {
            int row = tblDSTaiKhoan.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(null, "Bạn chưa chọn tài khoản nào để xóa!");
            } else {
                int response = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa không?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
                if (response == JOptionPane.YES_OPTION) {
                    String supplierCode = tblDSTaiKhoan.getValueAt(row, 2).toString();
                    if (tkDao.delete(supplierCode)) {
                        DefaultTableModel dataModel = (DefaultTableModel) tblDSTaiKhoan.getModel();
                        dataModel.removeRow(row);
                        JOptionPane.showMessageDialog(null, "Xóa thành công tài khoản!");
                    }
                }
            }
        } catch (RemoteException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        try {
            int row = tblDSTaiKhoan.getSelectedRow();
            if (row >= 0) {
                TaiKhoan acc = revertAccountFormTextFile();
                if (tkDao.update(acc)) {
                    //NhanVienImpl ds = new NhanVienImpl();
                    NhanVien nhanVien = nvDao.getNhanVienTheoMa(acc.getMaNhanVien().getMaNhanVien());
                    tblDSTaiKhoan.setValueAt(acc.getMaNhanVien().getMaNhanVien(), row, 0);
                    tblDSTaiKhoan.setValueAt(nhanVien.getTenNhanVien(), row, 1);
                    tblDSTaiKhoan.setValueAt(acc.getTenTaiKhoan(), row, 2);
                    tblDSTaiKhoan.setValueAt(acc.getMatKhau(), row, 3);
                    tblDSTaiKhoan.setValueAt(acc.getQuyenTruyCap(), row, 4);
                    JOptionPane.showMessageDialog(null, "Cập nhật thành công tài khoản!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Chưa chọn tài khoản nào để sửa", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
        } catch (RemoteException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed
