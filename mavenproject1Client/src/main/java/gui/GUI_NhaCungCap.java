    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        try {
            int row = tblDSNhaCungCap.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(null, "Bạn chưa chọn nhà cung cấp nào để xóa!");
            } else {
                int response = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa không?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
                if (response == JOptionPane.YES_OPTION) {
                    String supplierCode = tblDSNhaCungCap.getValueAt(row, 0).toString();

                    if (dSNCC.delete(supplierCode)) {
                        DefaultTableModel dataModel = (DefaultTableModel) tblDSNhaCungCap.getModel();
                        dataModel.removeRow(row);
                        JOptionPane.showMessageDialog(null, "Xóa nhà cung cấp thành công!");
                    }
                }
            }
        } catch (RemoteException e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnXoaActionPerformed

