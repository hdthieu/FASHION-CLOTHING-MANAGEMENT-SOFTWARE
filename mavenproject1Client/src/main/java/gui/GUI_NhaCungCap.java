    private void updateSupplierTableData() throws java.rmi.RemoteException {
//        NhaCungCapImpl supplierDAO = new NhaCungCapImpl();
        List<NhaCungCap> suppliers = dSNCC.docTuBang();

        dataModel.setRowCount(0);

        for (NhaCungCap supplier : suppliers) {
            String[] rowData = {
                supplier.getMaNhaCungCap(),
                supplier.getTenNhaCungCap(),
                supplier.getSdt(),
                supplier.getDiaChi()
            };
            dataModel.addRow(rowData);
        }
        tblDSNhaCungCap.setModel(dataModel);
    }

